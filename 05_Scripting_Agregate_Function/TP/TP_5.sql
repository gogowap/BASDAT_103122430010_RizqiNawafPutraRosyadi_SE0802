--Cari berapa jumlah film yang tersedia di bioskop ini.
SELECT COUNT(*) AS JUMLAH_FILM_TERSEDIA
FROM "FILM_103122430010";

--Cari berapa jumlah film yang sedang diputar dibioskop ini. Satu judul film hanya boleh dihitung satu kali.
SELECT COUNT(DISTINCT ID_FILM) AS JUMLAH_FILM_TAYANG
FROM "JADWAL_103122430010";

--Cari id_member yang sudah menonton lebih dari 2 kali. Lakukan hal yang sama untuk 3 kali, 4 kali dan 5 kali.
SELECT ID_MEMBER, COUNT(*) AS TOTAL_NONTON
FROM "TRANSAKSI_103122430010"
GROUP BY ID_MEMBER
HAVING COUNT(*) > 2;

--Cari member termuda dan jumlah film yang sudah ditontonnya.
--disini saya menggunakan Subquery
SELECT 
    M.ID_MEMBER, 
    M.NAMA, 
    M.TGL_LAHIR,
    COUNT(TR.KODE_PEMESANAN) AS JUMLAH_NONTON
FROM "MEMBER_103122430010" M
LEFT JOIN "TRANSAKSI_103122430010" TR ON M.ID_MEMBER = TR.ID_MEMBER
WHERE M.TGL_LAHIR = (SELECT MAX(TGL_LAHIR) FROM "MEMBER_103122430010")
GROUP BY M.ID_MEMBER, M.NAMA, M.TGL_LAHIR;

--Cari jumlah member yang lahir untuk setiap bulannya.
SELECT 
    TO_CHAR(TGL_LAHIR, 'Month') AS BULAN_LAHIR, 
    COUNT(*) AS JUMLAH_MEMBER
FROM "MEMBER_103122430010"
GROUP BY TO_CHAR(TGL_LAHIR, 'Month'), TO_CHAR(TGL_LAHIR, 'MM')
ORDER BY TO_CHAR(TGL_LAHIR, 'MM');

--Berapa jumlah jadwal tayang per film pada periode aktif?
SELECT 
    F.JUDUL, 
    COUNT(J.ID_JADWAL) AS JUMLAH_TAYANG
FROM "FILM_103122430010" F
JOIN "JADWAL_103122430010" J ON F.ID_FILM = J.ID_FILM
WHERE J.PERIODE_END >= SYSDATE -- Filter periode aktif
GROUP BY F.JUDUL;
